import mongoose from 'mongoose';
const schema = mongoose.Schema;

const RecipeSchema = new schema({
  name: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  instructions: {
    type: String,
    required: true
  },
  createdDate: {
    type: Date,
    default: Date.now
  },
  likes: {
    type: Number,
    default: 0
  },
  username: {
    type: String,
    required: true
  },
});

export const Recipe = mongoose.model('Recipe', RecipeSchema);
